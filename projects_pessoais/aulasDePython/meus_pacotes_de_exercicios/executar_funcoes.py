def somar():
    num1 = float(input('Digite o primeiro número para ser realizada a soma: '))
    num2 = float(input('Digite o segundo número para ser realizada a soma: '))
    soma = num1 + num2
    return f'O resultado da soma, é: {soma}'


def subtrair():
    num1 = float(input('Digite o primeiro número para ser realizada a subtração: '))
    num2 = float(input('Digite o segundo número para ser realizada a subtração: '))
    subtracao = num1 - num2
    return f'O resultado da sua subtração, é: {subtracao}'

def multiplicar():
    num1 = float(input('Digite o primeiro número para ser realizada a multiplicação: '))
    num2 = float(input('Digite o segundo número para ser realizada a multiplicação: '))
    multiplicacao = num1 * num2
    return  f'O resultado da sua multiplicação, é: {multiplicacao}'

def dividir():
    num1 = float(input('Digite o primeiro número para ser realizada a divisão: '))
    num2 = float(input('Digite o segundo número para ser realizada a divisão: '))
    divisao = num1 / num2
    return  f'O resultado da sua divisão, é: {divisao}'


